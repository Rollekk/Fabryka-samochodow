package z12.Samochod;

public class Kola {
private int wielkoscFelgi;
private String materialKol;
private double rozstawKol;
private int iloscSrub;
private int szerokoscOpony;
private int wysokoscOpony;

    public int getWielkoscFelgi() {
        return wielkoscFelgi;
    }

    public String getMaterialKol() {
        return materialKol;
    }

    public double getRozstawKol() {
        return rozstawKol;
    }

    public int getIloscSrub() {
        return iloscSrub;
    }

    public int getSzerokoscOpony() {
        return szerokoscOpony;
    }

    public int getWysokoscOpony() {
        return wysokoscOpony;
    }

    public void setWielkoscFelgi(int wielkoscFelgi) {
        this.wielkoscFelgi = wielkoscFelgi;
    }

    public void setMaterialKol(String materialKol) {
        this.materialKol = materialKol;
    }

    public void setRozstawKol(double rozstawKol) {
        this.rozstawKol = rozstawKol;
    }

    public void setIloscSrub(int iloscSrub) {
        this.iloscSrub = iloscSrub;
    }

    public void setSzerokoscOpony(int szerokoscOpony) {
        this.szerokoscOpony = szerokoscOpony;
    }

    public void setWysokoscOpony(int wysokoscOpony) {
        this.wysokoscOpony = wysokoscOpony;
    }
}
